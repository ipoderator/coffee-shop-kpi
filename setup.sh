# –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–∞—è —É—Å—Ç–∞–Ω–æ–≤–∫–∞ tailwindcss –µ—Å–ª–∏ –Ω—É–∂–Ω–æ
